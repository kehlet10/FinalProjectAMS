// Initializes (resets) the display
void DisplayInit()
{
	//Controlpins skal være input
	DDRG |= 0b00000111;
	DDRD |= 0b10000000;
	//Data pins skal være outputs
	DDRA = 0xFF;
	DDRC = 0xFF;
	
	PORTG |= 0b00000111;
	PORTD |= 0b10000000;
	
	RST_PORT &= ~(1<<RST_BIT);
	
	_delay_ms(500);
	
	RST_PORT |= (1<<RST_BIT);
		
	_delay_ms(130);
	
	SleepOut();

	DisplayOn();
	
	
	MemoryAccessControl(0b00001000);
	InterfacePixelFormat(0b00000101);
}

